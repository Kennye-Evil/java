    public void RellenarTabla()
{
     NegocioMarcas n1 = new NegocioMarcas();
    List<EncapsulamientoMarcas> listNiveles = n1.mostrarMarca();
    
    DefaultTableModel modeloTabla = (DefaultTableModel) tableniveles.getModel();
    
    while(modeloTabla.getRowCount()>0)
    {
        
    modeloTabla.removeRow(0);
    
    }
    
    for(EncapsulamientoMarcas nivel : listNiveles)
    {
       Object[] Datofila = 
        {
            nivel.getId(),
                nivel.getDescripcion(),
        };
       modeloTabla.addRow(Datofila);
    }
    tableniveles.setModel(modeloTabla);
}

