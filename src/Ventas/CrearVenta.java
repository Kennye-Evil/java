    public void ComboBoxVendedores(){
        
        cbxvendedor.removeAllItems();
        NegocioVendedores n1 = new NegocioVendedores();
        List<EncapsulamientoVendedores>listaVendedores = n1.MostrarVendedores();
        for(EncapsulamientoVendedores vendedor : listaVendedores){
           
            this.cbxvendedor.addItem(vendedor.getId()+"-"+vendedor.getNombre());
            
        }
    }
    
     public void ComboBoxClientes(){
        
        cbxcliente.removeAllItems();
         NegocioClientes n1 = new NegocioClientes();
        List<EncapsulamientoClientes>listaclientes = n1.mostrarClientes();
        for(EncapsulamientoClientes cliente : listaclientes){
           
            this.cbxcliente.addItem(cliente.getId()+"-"+cliente.getNombre());
            
        }
    }
      public void ComboBoxAutos(){
        
        cbxauto.removeAllItems();
        NegocioAutos n1 = new NegocioAutos();
        List<EncapsulamientoAutos>listaAutos = n1.mostrarAutos();
        for(EncapsulamientoAutos autos : listaAutos){
           
            this.cbxauto.addItem(autos.getId()+"-"+autos.getModelo());
            
        }
    }
     public void ComboBoxpago(){
        
        cbxpago.removeAllItems();
         NegocioTiposdepago n1 = new NegocioTiposdepago();
        List<EncapsulamientoTiposdepago>listapagos = n1.mostrarPagos();
        for(EncapsulamientoTiposdepago pago : listapagos){
           
            this.cbxpago.addItem(pago.getId()+"-"+pago.getDescripcion());
            
        }
    }
     void fecha(){
         DateFormat df = DateFormat.getDateInstance();
         Date FechaActual = new Date();
         this.jfecha.setDate(FechaActual);
         txtfecha.setText(df.format(FechaActual));
         
     }

    
    
    
    
