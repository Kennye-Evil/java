    private void btnañadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnañadirActionPerformed

        
                
                String nombrevendedor = this.cbxvendedor.getSelectedItem().toString();
                String[] partesV = nombrevendedor.split("-");
                int idvendedor = Integer.parseInt(partesV[0]);
                
                String nombrecliente = this.cbxcliente.getSelectedItem().toString();
                String[] partesC = nombrecliente.split("-");
                int idcliente = Integer.parseInt(partesC[0]);
                
                String nombreauto = this.cbxauto.getSelectedItem().toString();
                String[] partesA = nombreauto.split("-");
                int idAuto = Integer.parseInt(partesA[0]);

                String tipopago = this.cbxpago.getSelectedItem().toString();
                String[] partesP = tipopago.split("-");
                int idPago = Integer.parseInt(partesP[0]);


               
         String fecha = txtfecha.getText();
        EncapsulamientoVentas v1 = new EncapsulamientoVentas();
        v1.setVendedor_id(idvendedor);
        v1.setCliente_id(idcliente);
        v1.setAuto_id(idAuto);
        v1.setTipopago(tipopago);
        v1.setFecha(fecha);
        
        NegocioVentas n1= new NegocioVentas();
        String Respuesta = n1.crearVenta(v1);
        
        JOptionPane.showMessageDialog(null, Respuesta);
        
        
    }//GEN-LAST:event_btnañadirActionPerformed

