    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed

        // TODO add your handling code here:
        
        EncapsulamientoAutos n1 = new EncapsulamientoAutos();
        n1.setId(Integer.parseInt(lblrID.getText()));
        n1.setAno(Integer.parseInt(txtano.getText()));
         n1.setColor(txtcolor.getText());
         n1.setModelo(txtmodelo.getText());
         n1.setPrecio(Integer.parseInt(txtprecio.getText()));
         n1.setSerie(txtserie.getText());
///      n1.setDescripcion(txtdescripcion.getText());

        NegocioAutos n2 = new NegocioAutos();
        boolean resultado = n2.editarAutos(n1);
        
        if(resultado == true)
        {
            JOptionPane.showMessageDialog(null, "registro actualizado");
        }
        ModificacionAutos m1 = new ModificacionAutos();
        m1.show();
        this.dispose();
        
    }//GEN-LAST:event_btnactualizarActionPerformed

