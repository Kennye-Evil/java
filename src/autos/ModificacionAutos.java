public void RellenarTabla()
{
    NegocioAutos n1 = new NegocioAutos();
    List<EncapsulamientoAutos> listautos = n1.mostrarAutos();
    
    DefaultTableModel modeloTabla = (DefaultTableModel) tableniveles.getModel();
    
    while(modeloTabla.getRowCount()>0)
    {
        
    modeloTabla.removeRow(0);
    
    }
    
    for(EncapsulamientoAutos autos : listautos)
    {
       Object[] Datofila = 
        {
            autos.getId(),
            autos.getSerie(),
            autos.getModelo(),
            autos.getAno(),
            autos.getPrecio(),
            autos.getColor()
        };
       modeloTabla.addRow(Datofila);
    }
    tableniveles.setModel(modeloTabla);
}
